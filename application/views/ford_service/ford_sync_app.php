                    sistemul poate citi cu voce tare mesajele tale text. Ford SYNC AppLink face toate aceste lucruri
                    şi &icirc;ţi permite &icirc;n plus să accesezi aplicaţii din mers. Aceste două tehnologii sunt
                    separate. Aşa că, dacă dispui de echipamentul hardware SYNC iniţial, nu &icirc;i poţi face upgrade
                    pentru a fi compatibil cu AppLink. <br>
