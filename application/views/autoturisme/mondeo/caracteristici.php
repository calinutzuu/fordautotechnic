            multe tehnologii Ford care in trecut au fost premiate de Euro NCAP pentru inovatii in domeniul sigurantei.
            Iar aceasta masina este prima din gama Ford care va fi echipata cu centuri de siguranta cu airbag
            incorporat, pentru locurile exterioare din spate. Descopera mai jos mai multe despre acestea.</p>
        </p>
    </header>
    <article class="content-column left">
        <div class="img-column"> <img
