            <p>Noul sistem de inchidere centralizata a usilor cu telecomanda este mai sigur ca niciodata. Si aceasta
                deoarece poti alege sa descui o singura usa, iar celelalte raman blocate, pana cand activezi un
                comutator de pe portiera soferului. Acelasi comutator controleaza blocarea si deblocarea tuturor usilor
                vehiculului.</p>
            </p>
