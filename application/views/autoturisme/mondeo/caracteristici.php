            <h3 class="bigHeading02">
                Mai multa siguranta
            </h3>

            <p>

            <p>Coliziunile in trafic la viteza mica, din cauza opririlor si pornirilor frecvente, au loc mult prea des.
                Active City Stop este o tehnologie avansata proiectata pentru a te ajuta sa le eviti sau, cel putin, sa
                reduci impactul. Daca sistemul detecteaza ca masina din fata s-a oprit brusc, iar tu te deplasezi cu mai
                putin de 40 km/h, va actiona automat franele. Ca dovada a caracterului sau inovator, aceasta tehnologie
                a primit premiul &rdquo;Advanced&rdquo; oferit de Euro NCAP pentru siguranta.*<br><br><font size="1">*Acordat
                    pentru Ford Focus.</font></p>
            </p>
        </div>
        <div class="content-block">
            <img
                    src="/assets/img/mondeo/1214487218114.jpg"
                    alt="Sistem de asistenta la pornirea din rampa"/><span class="fv-overlay-icon"></span>

            <h3 class="bigHeading02">
                Un plus de control in cazul pornirilor din rampa
            </h3>

            <p>

            <p>Sistemul de asistenta la pornirea din rampa impiedica temporar alunecarea in spate sau in fata atunci
                cand pornesti din rampa. Sistemul functioneaza prin mentinerea presiunii la nivelul sistemului de fanare
                pentru un interval suplimentar de 2,5 secunde, asigurandu-ti mai mult timp si control atunci cand muti
                piciorul de pe pedala de frana pe cea de acceleratie. Acest sistem este, de asemenea, util cand opresti
                si pornesti autovehiculul pe suprafete alunecoase.</p>
            </p>
        </div>
        <div class="content-block last">
            <img
                    src="/assets/img/mondeo/1214487218614.jpg"
                    alt="Sistem de asistenta la franarea de urgenta (EBA)"/><span class="fv-overlay-icon"></span>

            <h3 class="bigHeading02">
                Putere suplimentara pentru franarea de urgenta
            </h3>

            <p>

            <p>Sistemul de asistenta la franarea de urgenta (EBA) ajuta vehiculul sa atinga eficienta maxima de franare
                in cazul unei situatii extreme. Este controlat automat prin intermediul sistemului ESP, care detecteaza
                presiunea aplicata de sofer pe pedala de frana si creste automat aceasta presiune, daca este cazul.</p>
            </p>
        </div>
    </article>
    <article class='three-column'>
        <div class="content-block">
            <img src="#" alt=""/>

            <h3 class="bigHeading02">
                Un sistem care poate ajuta la protejarea pietonilor
            </h3>

            <p>

            <p>Sistemul de asistenta pre-coliziune cu detectare a pietonilor detecteaza persoanele de pe drumul din fata
                sau pe cei care s-ar putea intersecta cu traseul vehiculului si te avertizeaza cu privire la prezenta
                acestora. Daca nu reactionezi, sistemul va aplica automat franele. Aceasta noua tehnologie este
                conceputa pentru a ajuta la reducerea severitatii accidentelor, sau pentru a ajuta la evitarea complet a
                acestora, functionand la viteze de pana la 80 km/h.</p>
            </p>
        </div>
        <div class="content-block">
            <img src="#" alt=""/>

            <h3 class="bigHeading02">
                Stabileste in avans viteza maxima
            </h3>

            <p>

            <p>Un dispozitiv optional de limitare activa a vitezei (dotare standard pe Titanium) iti permite sa setezi
                in avans viteza maxima pentru o intreaga calatorie. Sistemul te avertizeaza daca anulezi setarea sau
                daca depasesti viteza setata in mod accidental, de exemplu atunci cand cobori o panta. Acest dispozitiv
                este util in mod special in zone cu restrictii de viteza, deoarece iti permite sa te concentrezi asupra
                drumului in loc sa verifici in mod constant vitezometrul.</p>
            </p>
        </div>
        <div class="content-block last">
            <img src="#" alt=""/>

            <h3 class="bigHeading02">
                Opriri de urgenta mai rapide si mai scurte
            </h3>

            <p>

            <p>Atunci cand detecteaza o situatie de oprire de urgenta, ESP pre-incarca sistemul de franare, inchizand
                orice gol de aer dintre placutele si discurile de frana. Cunoscuta drept pre-activare electronica a
                franelor, aceasta tehnologie ajuta la reducerea distantei de franare a masinii.</p>
            </p>
        </div>
