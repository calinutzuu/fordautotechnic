        </div>
        <div class="content">
            <h3 class="bigHeading02">
                Un plus de protectie pentru pasagerii din spate
            </h3>

            <p>

            <p>Cele doua scaune din spate, dinspre exterior pot fi echipate cu o caracteristica de siguranta unica: o
                centura de siguranta cu airbag incorporat. In eventualitatea unui impact, airbag-urile se declanseaza si
                distribuie fortele generate de coliziune pe o zona a corpului de patru ori mai mare decat centurile
                conventionale. Prin reducerea presiunii exercitate asupra pieptului si controland mai bine miscarea
                capului si a gatului, aceasta tehnologie inovatoare poate reduce probabilitatea vatamarii.</p>
            </p>
        </div>
    </article>
    <article class='two-column'>
        <div class="content-block">
            <img
                    src="/assets/img/mondeo/1214487217695.jpg"
                    alt="Ford SYNC 2 cu asistenta de urgenta"/><span class="fv-overlay-icon"></span>

            <h3 class="bigHeading02">
                Ford SYNC 2 cu asistenta de urgenta
            </h3>

            <p>

            <p>Un aspect important al tehnologiei Ford SYNC 2 care iti poate salva viata este asistenta de urgenta. In
                cazul unui accident, cand se activeaza airbagul sau se dezactiveaza pompa de carburant, sistemul
                efectueaza automat un apel direct catre serviciile de urgenta. Daca este cazul, poti alege sa intrerupi
                apelul sau sa discuti personal cu operatorul. Un aspect extrem de important este faptul ca sistemul va
                furniza informatii vitale, precum coordonatele GPS, chiar si atunci cand ti-ai pierdut
                cunostinta.*<br><br><font size="1">*Asistenta de urgenta Ford functioneaza in peste 30 de tari din
                    Europa. Aceasta caracteristica functioneaza atunci cand este conectata cu un telefon mobil
                    compatibil pe care il ai la tine, in momentul in care un airbag este declansat (cu exceptia
                    airbagurilor pentru genunchi) sau atunci cand un senzor care inregistreaza o coliziune intrerupe
                    pompa de combustibil. Functia de mesagerie si asistenta de urgenta nu sunt acceptate in toate
                    limbile europene.&nbsp;</font></p>
            </p>
        </div>
        <div class="content-block last">
            <img
                    src="/assets/img/mondeo/1214487222719.jpg"
                    alt="Ofera incredere chiar si in conditii dificile"/><span class="fv-overlay-icon"></span>

            <h3 class="bigHeading02">
                Ofera incredere chiar si in conditii dificile
            </h3>

            <p>

            <p>Masina este prevazuta cu un sistem de Control electronic al stabilitatii (ESP), care include o serie de
                tehnologii concepute pentru a te ajuta sa mentii controlul. Senzorii detecteaza orice schimbari bruste
                de directie, franare sau comportament al vehiculului care te-ar putea face sa pierzi controlul, pentru
                ca apoi ESP sa ajusteze precis viteza fiecarei roti in parte, facand masina mai stabila si ajutand la
                prevenirea alunecarii. Caracteristicile ESP includ asistenta la pornirea in rampa, asistenta la franarea
                de urgenta si pre-activarea electronica a franelor.</p>
            </p>
        </div>
    </article>
