            <h3 class="bigHeading02">
                Intrari si iesiri din parcare fara stres
            </h3>

            <p>

            <p>Cu ajutorul tehnologiilor Ford, sistemul de asistenta activa la parcare face mai mult decat sa te
                directioneze in locurile de parcare paralela. Sistemul de asistenta la iesirea din parcare (POA) ofera
                un plus de siguranta. Sistemul pentru parcare perpendiculara (PPA) poate sa detecteze daca masina are
                loc intr-un anumit spatiu si te conduce in marsarier, in locul respectiv. Iar daca exista obstacole,
                sistemul de asistenta la parcare laterala (SPA) te atentioneaza.</p>
            </p>
        </div>
