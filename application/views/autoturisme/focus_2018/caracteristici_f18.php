        <div class="content">
            <h3 class="bigHeading02">
                Un sistem care poate reduce gravitatea accidentelor sau le poate evita
            </h3>

            <p>

            <p>Sistemul de asistenta pre-coliziune detecteaza vehiculele si pietonii de pe drumul din fata sau pe cei
                care s-ar putea intersecta cu traseul vehiculului si te avertizeaza cu privire la prezenta acestora.
                Daca nu reactionezi, sistemul aplica automat franele. Aceasta tehnologie e conceputa pentru a ajuta sa
                reduci riscul accidentelor sau pentru a le evita complet, functionand la viteze de pana la 80 km/h.</p>
            </p>
        </div>
    </article>
    <article class="content-column right">
        <div class="content">
            <h3 class="bigHeading02">
                Te ajuta sa franezi la viteze mai mari
            </h3>

            <p>

            <p>Sistemul de franare activa reprezinta o dotare de siguranta inovatoare, care functioneaza atunci cand te
                deplasezi cu viteze mai mari de 50 km/h. Tehnologia utilizeaza un RADAR pentru a scana drumul din fata
                si, daca stabileste ca este posibila o coliziune, te va avertiza. Daca nu reactionezi, sistemul
                pregateste frana si incepe sa franeze pentru a te ajuta sa incetinesti. La viteze mai mici de 50 km/h, o
                alta tehnologie iti sta la dispozitie pentru a te ajuta: Oprire activa in oras. Combinate, aceste doua
                tehnologii iti ofera siguranta asistentei la franare la toate vitezele.</p>
            </p>
        </div>
