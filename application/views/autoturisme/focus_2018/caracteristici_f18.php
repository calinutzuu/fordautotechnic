                Informatii importante pe scurt
            </h3>

            <p>

            <p>Afisajul de la nivelul capului proiecteaza informatiile vehiculului tau in campul vizual, astfel incat
                ramani la curent fara a trebui sa iti iei ochii de la drum, privind in jos la tabloul de bord.
