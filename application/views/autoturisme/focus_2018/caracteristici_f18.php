                alt="Ford SYNC 2"/><span class="fv-overlay-icon"></span></div>
        <div class="content">
            <h3 class="bigHeading02">
                Asistentul tau personal in timpul condusului
            </h3>

            <p>

            <p>Ford Co-Pilot360 combina o gama de tehnologii care, la un loc, iti fac experienta de condus mai
                confortabila
                si mai placuta ca niciodata. De exemplu, tempomatul adaptiv cu „Stop & Go” iti regleaza in mod automat
                viteza de deplasare pe baza vitezei vehiculului din fata. Daca traficul incetineste pana se opreste de
                tot,
                motorul se va opri in mod automat si va reporni atunci cand drumul va fi liber. Sistemul poate chiar sa
                recunoasca indicatoarele de viteza si iti va regla viteza in mod corespunzator. Tehnologia de centrare
                pe
                banda poate oferi de asemenea, asistenta de directie, respectand marcajele drumului pentru a te ajuta sa
                te
                pozitionezi pe mijlocul benzii pe care te afli.</p>
            </p>
        </div>
    </article>
    <article class="content-column right">
        <div class="content">
            <h3 class="bigHeading02">
                Intrari si iesiri din parcare fara stres
            </h3>

            <p>

            <p>Asistenta activa la parcare de cea mai recenta generatie nu doar te directioneaza in locurile de parcare,
                ci
                si controleaza acceleratia, schimbarea vitezelor si franarea. Aceasta tehnologie inteligenta,
                disponibila la
                modelele Focus, face ca parcarea sa fie lipsita de orice efort. Dupa activare, este suficient sa
                sellectezi
                o parte a drumului si daca doresti o parcare paralela sau perpendiculara. Apoi, apasa si mentine apasat
                butonul si lasa masina sa faca toata treaba in locul tau.</p>
            </p>
