            Siguranta ta. Prioritatea noastra.
        </h1>
        <span class="highlighter"></span>

        <p class="intro-text">

        <p>O serie de tehnologii inteligente sunt concepute pentru a te proteja, pe tine, familia ta si colegii de
            trafic. De exemplu, franarea activa si Sistemul de asistenta pre-coliziune cu detectarea pietonilor si
            ciclistilor, te pot avertiza in privinta eventualelor coliziuni si pot frana automat, daca tu nu reactionezi
            la timp.</p>
