    <header class="intro-header">
        <h1 class="bigHeading">
            Tehnologie cu un scop
        </h1>
        <span class="highlighter"></span>

        <p class="intro-text">

        <p>Noul Focus e construit sa-ti imbunatateasca experienta la volan, in toate modurile posibile.
            <br><br>
            Aceasta masina de urmatoarea generatie e plina de caracteristici avansate, cum ar fi tehnologii de asistenta
            a
            soferului, inclusiv tempomat adaptiv, cu functie de pornire-oprire si centrare pe banda de mers, si
            Asistenta
            activa la parcare, versiunea 2. Impreuna, aceste tehnologii inteligente te pot ajuta sa gestionezi
            acceleratia,
            franarea si chiar directia. Vorbim de tehnologie care implica in asa masura lipsa efortului, incat te vei
            mira
            cum te-ai descurcat pana acum fara aceasta.</p>
        </p>
    </header>
    <article class="content-column left">
        <div class="img-column"><img
