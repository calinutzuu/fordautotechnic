    <h1 class="bigHeading">
        O masina in care detii controlul
    </h1>
    <span class="highlighter"></span>

    <p class="intro-text">

    <p>Multumita designului si tehnologiei inteligente, modelul Focus face ca fiecare minut petrecut in masina sa fie
        cat mai confortabil, sigur si placut cu putinta. <br>
        <br>
        Tehnologiile inovatoare te asigura, de asemenea, ca esti permanent conectat la drum, dar si la muzica, mesaje si
        apeluri telefonice, chiar si in miscare.</p>
    </p>
</header>
